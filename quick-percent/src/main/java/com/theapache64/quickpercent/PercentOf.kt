package com.theapache64.quickpercent


/**
 * ###### INT ##########
 */

fun Int.percentOf(of: Int): Int {
    return (of / 100) * this
}


fun Int.percentOf(of: Float): Float {
    return (of / 100) * this
}


fun Int.percentOf(of: Long): Long {
    return (of / 100) * this
}


fun Int.percentOf(of: Double): Double {
